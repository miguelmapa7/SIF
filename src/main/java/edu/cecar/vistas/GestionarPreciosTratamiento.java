    public static ControladorPrecio getControladorPrecio() {

        if (controladorPrecio == null) {
            controladorPrecio = new ControladorPrecio();
        }

        return controladorPrecio;

    }

    public void guardar(Precio precio) {

        try {

            getControladorPrecio().guardar(precio);
            JOptionPane.showMessageDialog(null,
                    "Precio Almacenado correctamente");
            nuevoPrecio();
            alInicio();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null,
                    "Error al almacenar el Precio");
            //e.printStackTrace();
        }

    }

    public void nuevoPrecio() {
        jTPrecio.setText("");
        jTCodigoAreaCuerpo.setText("");
        jTValor.setText("");
        jTPrecio.grabFocus();
    }

    public void alInicio() {
        jTCodigoAreaCuerpo.setEnabled(false);
        jTValor.setEnabled(false);
        jBNuevo.setEnabled(false);
        jBRegistar.setEnabled(false);
    }

    public void despuesInicio() {
        jTCodigoAreaCuerpo.setEnabled(true);
        jTValor.setEnabled(true);
        jBNuevo.setEnabled(true);
        jBRegistar.setEnabled(true);
    }

    public void consultarPrecio() {
        if (jTPrecio.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite Id Precio");
        } else {
            try {
                // Se consulta la existencia del usuario en la BD
                Precio precio
                        = getControladorPrecio()
                                .consultar(jTPrecio.getText());

                if (precio != null) {
                    jTCodigoAreaCuerpo.setText(precio.getiDAreaCuerpo() + "");
                    jTValor.setText(precio.getValor());

                    int resp = JOptionPane.showConfirmDialog(null,
                            "¿Desea Modificar Precio?", "Alerta!",
                            JOptionPane.YES_NO_OPTION);
                    if (resp == 0) {
                        // Se habilitan los campos para módificar 
                        despuesInicio();
                    } else {
                        // NO hace nada
                    }
                } else {
                    despuesInicio();
                }

            } catch (Exception e1) {
                // TODO Auto-generated catch block
                //e1.printStackTrace();
                JOptionPane.showMessageDialog(null, "Precio NO existe");
            }
        }

    }

