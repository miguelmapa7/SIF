    private void jBRegistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistarActionPerformed

        // Se procede a realizar operaciones con los datos 
        if (jTPrecio.getText().equals("")
                || jTCodigoAreaCuerpo.getText().equals("")
                || jTValor.getText().equals("")) {

            JOptionPane.showMessageDialog(null,
                    "Verificar los datos ingresados");
        } else {

            Precio precio
                    = new Precio(Integer.parseInt(
                            jTPrecio.getText()),
                            Integer.parseInt(jTCodigoAreaCuerpo.getText()),
                            jTValor.getText());
            guardar(precio);
        }

    }//GEN-LAST:event_jBRegistarActionPerformed

